package co.edu.udea.program1.Common.Models;

public class Correlation {
	public double Correlation;
	public double CoefficientDetermination;
	
	
	
	public double getCorrelation() {
		return Correlation;
	}
	public void setCorrelation(double correlation) {
		Correlation = correlation;
	}
	public double getCoefficientDetermination() {
		return CoefficientDetermination;
	}
	public void setCoefficientDetermination(double coefficientDetermination) {
		CoefficientDetermination = coefficientDetermination;
	}
}
